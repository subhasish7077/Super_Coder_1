for i in range(1,101):
    print(i,end=" ")

print("\nOdd numbers")
print("---------------------------------------------------------")
for i in range(1,101,2):
    print(i,end=" ")


print("\neven numbers")
print("---------------------------------------------------------")
for i in range(2,101,2):
    print(i,end=" ")


print("\n100-1")
print("---------------------------------------------------------")
for i in range(100,0,-1):
    print(i,end=" ")


print("\nreverse Odd numbers")
print("---------------------------------------------------------")
for i in range(99,0,-2):
    print(i,end=" ")

print("\nreverse even numbers")
print("---------------------------------------------------------")
for i in range(100,1,-2):
    print(i,end=" ")

